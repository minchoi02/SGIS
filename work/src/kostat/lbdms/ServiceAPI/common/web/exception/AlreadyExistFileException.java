package kostat.lbdms.ServiceAPI.common.web.exception;

public class AlreadyExistFileException extends FileSystemException {

	private static final long serialVersionUID = -4799657620518693256L;
	@SuppressWarnings("unused")
	private String path;
	
	public AlreadyExistFileException( String message ) {
		super( message );
	}
	
	public AlreadyExistFileException( String message, String path ) {
		super( message );
		this.path = path;
	}

	@Override
	public String getMessage() {
		
		return super.getMessage();
	}
	
}