package kostat.lbdms.ServiceAPI.common.web.exception;


public class PermissionException extends FileSystemException {

	private static final long serialVersionUID = 2436261212126065497L;
	
	@SuppressWarnings("unused")
	private String path;
	
	public PermissionException( String message ) {
		super( message );
	}
	
	public PermissionException( String message, String path ) {
		super( message );
		this.path = path;
	}

	@Override
	public String getMessage() {
		
		return super.getMessage();
	}

}
