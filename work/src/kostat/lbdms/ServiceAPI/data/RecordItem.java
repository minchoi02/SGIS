package kostat.lbdms.ServiceAPI.data;

import java.util.ArrayList;

public class RecordItem {
	
	private String x;
	private String y;
	private String totOaCode;
	private String admDrCode;
	private String baseCode;
	private String baseX;
	private String baseY;
	private String totX;
	private String totY;
	private String mappingAddr; // 도로명
	private String mappingAddr2; // 지번
	private String mappingStatus;
	
	private String mgt;
	private String pnu;
	private String daumFailCode;

	
	private boolean isMapped;
	
	private ArrayList<String> valueList;
	
	public RecordItem()
	{
		valueList = new ArrayList<String>();
		isMapped = false;
		
		totOaCode = "";
		admDrCode = "";
		baseCode = "";
		baseX = "";
		baseY = "";
		totX = "";
		totY = "";
		mappingAddr = "";
		mappingAddr2 = "";
		mappingStatus = "";
		daumFailCode = "";
	}
	
	public ArrayList<String> getValueList()
	{
		return this.valueList;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}

	public boolean isMapped() {
		return isMapped;
	}

	public void setMapped(boolean isMapped) {
		this.isMapped = isMapped;
	}

	public String getTotOaCode() {
		return totOaCode;
	}

	public void setTotOaCode(String totOaCode) {
		this.totOaCode = totOaCode;
	}

	public String getAdmDrCode() {
		return admDrCode;
	}

	public void setAdmDrCode(String admDrCode) {
		this.admDrCode = admDrCode;
	}

	public String getBaseCode() {
		return baseCode;
	}

	public void setBaseCode(String baseCode) {
		this.baseCode = baseCode;
	}

	public String getBaseX() {
		return baseX;
	}

	public void setBaseX(String baseX) {
		this.baseX = baseX.split("\\.")[0];
	}

	public String getBaseY() {
		return baseY;
	}

	public void setBaseY(String baseY) {
		this.baseY = baseY.split("\\.")[0];
	}

	public String getTotX() {
		return totX;
	}

	public void setTotX(String totX) {
		
		this.totX = totX.split("\\.")[0];
	}

	public String getTotY() {
		return totY;
	}

	public void setTotY(String totY) {
		this.totY = totY.split("\\.")[0];
	}

	public String getMappingAddr() {
		return mappingAddr;
	}

	public void setMappingAddr(String mappingAddr) {
		this.mappingAddr = mappingAddr;
	}
	
	public String getMappingAddr2() {
		return mappingAddr2;
	}

	public void setMappingAddr2(String mappingAddr2) {
		this.mappingAddr2 = mappingAddr2;
	}

	public String getMappingStatus() {
		return mappingStatus;
	}

	public void setMappingStatus(String mappingStatus) {
		this.mappingStatus = mappingStatus;
	}
	
	public String getMgt() {
		return mgt;
	}

	public void setMgt(String mgt) {
		this.mgt = mgt;
	}
	
	public String getPnu() {
		return pnu;
	}

	public void setPnu(String pnu) {
		this.pnu = pnu;
	}

	public String getDaumFailCode() {
		return daumFailCode;
	}

	public void setDaumFailCode(String daumFailCode) {
		this.daumFailCode = daumFailCode;
	}
	
	
}
